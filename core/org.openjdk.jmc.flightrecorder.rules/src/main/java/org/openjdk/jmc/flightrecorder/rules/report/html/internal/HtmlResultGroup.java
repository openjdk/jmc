package org.openjdk.jmc.flightrecorder.rules.report.html.internal;

import java.util.Collection;
import java.util.List;

/**
 * Provides structure for how results are to be rendered. This is primarily a tree structure where
 * names and images are associated with topics. These topics can in turn be associated with results
 * via the {@link HtmlResultProvider} interface.
 */
public interface HtmlResultGroup {
	List<HtmlResultGroup> getChildren();

	boolean hasChildren();

	/**
	 * @return Return an id for this group. May be generated but should be unique within a tree of
	 *         groups.
	 */
	String getId();

	String getName();

	/**
	 * @return a string with base64 encoded image data or null if no image is available
	 */
	String getImage();

	Collection<String> getTopics();
}
