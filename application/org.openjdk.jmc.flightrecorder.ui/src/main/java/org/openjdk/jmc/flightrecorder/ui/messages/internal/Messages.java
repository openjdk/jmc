	private Messages() {
	}

	public static String stackTraceMessage(int itemCount, int totalCount, String frameFraction) {
		String message;
		if (itemCount == 1) {
			message = Messages.STACKTRACE_VIEW_TRACE_OF_TOTAL;
		} else {
			message = Messages.STACKTRACE_VIEW_TRACES_OF_TOTAL;
		}
		return NLS.bind(message, new Object[] {itemCount, frameFraction, totalCount});
	}

	public static String siblingMessage(int itemsInSiblings, int nSiblings) {
		String message;
		if (itemsInSiblings == 1) {
			if (nSiblings == 1) {
				message = Messages.STACKTRACE_VIEW_TRACE_IN_GROUP;
			} else {
				message = Messages.STACKTRACE_VIEW_TRACE_IN_GROUPS;
			}
		} else {
			if (nSiblings == 1) {
				message = Messages.STACKTRACE_VIEW_TRACES_IN_GROUP;
			} else {
				message = Messages.STACKTRACE_VIEW_TRACES_IN_GROUPS;
			}
		}
		return NLS.bind(message, itemsInSiblings, nSiblings);
	}
