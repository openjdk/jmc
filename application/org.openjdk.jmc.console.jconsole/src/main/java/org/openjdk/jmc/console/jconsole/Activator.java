package org.openjdk.jmc.console.jconsole;

import java.io.File;
import java.util.logging.Logger;

import org.eclipse.jface.preference.IPreferenceStore;
import org.eclipse.jface.resource.ImageDescriptor;
import org.eclipse.ui.plugin.AbstractUIPlugin;
import org.osgi.framework.BundleContext;

import org.openjdk.jmc.console.jconsole.preferences.PreferenceConstants;

/**
 * The activator class controls the plug-in life cycle
 */
public class Activator extends AbstractUIPlugin {
	// The plug-in ID
	public static final String PLUGIN_ID = "org.openjdk.jmc.console.jconsole"; //$NON-NLS-1$
	private static Logger LOGGER = Logger.getLogger(PLUGIN_ID);

	// The shared instance
	private static Activator plugin;

	/**
	 * The constructor
	 */
	public Activator() {
	}

	@Override
	public void start(BundleContext context) throws Exception {
		super.start(context);
		plugin = this;
	}

	@Override
	public void stop(BundleContext context) throws Exception {
		plugin = null;
		super.stop(context);
	}

	/**
	 * Returns the shared instance
	 *
	 * @return the shared instance
	 */
	public static Activator getDefault() {
		return plugin;
	}

	/**
	 * Returns an image descriptor for the image file at the given plug-in relative path
	 *
	 * @param path
	 *            the path
	 * @return the image descriptor
	 */
	public static ImageDescriptor getImageDescriptor(String path) {
		return imageDescriptorFromPlugin(PLUGIN_ID, path);
	}

	/**
	 * @return the logger used by this plug-in.
	 */
	public static Logger getLogger() {
		return LOGGER;
	}

	private static IPreferenceStore getPrefs() {
		return Activator.getDefault().getPreferenceStore();
	}

	/**
	 * @return the currently configured JConsole plug-in dir.
	 */
	public static File getJConsolePluginDir() {
		return new File(getPrefs().getString(PreferenceConstants.PROPERTY_JCONSOLE_PLUGIN_DIR));
	}

	public static long getUpdateInterval() {
		return getPrefs().getLong(PreferenceConstants.PROPERTY_JCONSOLE_UPDATE_INTERVAL);

	}
}
