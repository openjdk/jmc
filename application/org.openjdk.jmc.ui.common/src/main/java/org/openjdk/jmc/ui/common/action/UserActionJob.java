package org.openjdk.jmc.ui.common.action;

import org.eclipse.core.runtime.IProgressMonitor;
import org.eclipse.core.runtime.IStatus;
import org.eclipse.core.runtime.Status;
import org.eclipse.core.runtime.jobs.Job;
import org.eclipse.osgi.util.NLS;

import org.openjdk.jmc.ui.common.CorePlugin;

/**
 * Bridge class for running a user action as an Eclipse job.
 */
public class UserActionJob extends Job {
	private final IUserAction action;

	public UserActionJob(IUserAction action) {
		super(action.getName());
		this.action = action;
	}

	@Override
	protected IStatus run(IProgressMonitor monitor) {
		try {
			action.execute();
			monitor.done();
			return Status.OK_STATUS;
		} catch (Throwable e) {
			String message = e.getMessage() != null ? e.getMessage()
					: NLS.bind(Messages.ACTION_COULD_NOT_PERFORM, getName());
			return new Status(IStatus.ERROR, CorePlugin.PLUGIN_ID, message);
		}
	}

}
